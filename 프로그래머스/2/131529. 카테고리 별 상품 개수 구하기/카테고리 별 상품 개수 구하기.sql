SELECT SUBSTRING(PRODUCT_CODE,1,2) AS CATEGORY,
        COUNT(SUBSTRING(PRODUCT_CODE,1,2)) AS PRODUCT
FROM PRODUCT
GROUP BY SUBSTRING(PRODUCT_CODE,1,2)
ORDER BY 1
;












