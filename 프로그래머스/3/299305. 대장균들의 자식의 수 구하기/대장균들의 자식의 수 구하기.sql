SELECT  C.ID,
        COUNT(P.PARENT_ID) AS CHILD_COUNT
FROM ECOLI_DATA C LEFT JOIN ECOLI_DATA P
    ON C.ID = P.PARENT_ID
GROUP BY C.ID
ORDER BY C.ID
;












